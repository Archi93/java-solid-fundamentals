package arithmetics;

public class Basics {

    public static void main(String[] args) {
        // Część 1.
        // Korzystając z zaproponowanych operatorów zapisz wyrażenie zwracające oczekiwany rezultat.
        // W operacjach nie używaj liczby 1 i 0 chyba, że zostało to inaczej wskazane
        System.out.print("Wykorzystaj 1 raz '+' aby uzyskać liczbę 17. Twój wynik: ");
        System.out.println(12 + 5);

        System.out.print("Wykorzystaj 1 raz '-' aby uzyskać liczbę 7| ");
        System.out.println();

        System.out.print("Wykorzystaj 1 raz '*' aby uzyskać liczbę 9,6. Twój wynik: ");
        System.out.println();

        System.out.print("Wykorzystaj 1 raz '/' aby uzyskać liczbę 12. Twój wynik: ");
        System.out.println();

        System.out.print("Wykorzystaj 1 raz '/' aby uzyskać liczbę 3,14. Twój wynik: ");
        System.out.println();

        System.out.print("Wykorzystaj 1 raz '%` aby usykać liczbę 2. Twój wynik: ");
        System.out.println();

        System.out.print("Wykorzystaj 3 razy '+' aby uzyskać liczbę 22. Twój wynik: ");
        System.out.println();

        System.out.print("Wykorzystaj 3 razy '-' aby uzyskać liczbę 74. Twój wynik: ");
        System.out.println();

        System.out.print("Wykorzystaj 4 razy '*' aby uzyskać liczbę 6,3. Twój wynik: ");
        System.out.println();

        System.out.print("Wykorzystaj 2 razy '/' aby uzyskać liczbę 14. Twój wynik: ");
        System.out.println();

        System.out.print("Wykorzystaj 3 razy '%' aby uzyskać liczbę 5. Twój wynik: ");
        System.out.println();

        // Część 2.
        // W oparciu o zapisane działanie podaj ich wynik
        System.out.print("7 + -3 = ");
        System.out.println(4);

        System.out.print("13 - 9 = ");
        System.out.println();

        System.out.print("2.5 * 5 = ");
        System.out.println();

        System.out.print("17 / 2f = ");
        System.out.println();

        System.out.print("12 / 16 = ");
        System.out.println();

        System.out.print("-9 % 2 = ");
        System.out.println();

        System.out.print("7 + -3 + 3 + -3 = ");
        System.out.println();

        System.out.print("100 - 10 --10 = ");
        System.out.println();

        System.out.print("500 * 500 * 500 * 500 = ");
        System.out.println();

        System.out.print("19 / 4d / 2 = ");

        System.out.println();

        System.out.print("13 % 7 % 7 = ");
        System.out.println();

        // Część 3.
        // Po uzupełnieniu wartości dopisz do każdego działania linię System.out.println,
        // w której użyjesz wyrażenia z działania aby zweryfikować poprawność Twojego wyniku
        System.out.print("7 + -3 = ");
        System.out.println(4);
        System.out.println(7 + -3);
    }

}